func TestUnit_ObjectStoreAccessKey_Get(t *testing.T) {
	expected := client.ObjectStoreAccessKey{
		Name:        "test-account/test-user/key-id-abc",
		AccessKeyID: "AKIAIOSFODNN7EXAMPLE",
		Enabled:     true,
		User:        client.NamedReference{Name: "test-account/test-user"},
	}

	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		switch {
		case r.URL.Path == "/api/login":
			w.Header().Set("x-auth-token", "tok")
			w.WriteHeader(http.StatusOK)
		case r.Method == http.MethodGet && r.URL.Path == "/api/2.22/object-store-access-keys":
			name := r.URL.Query().Get("names")
			if name != "test-account/test-user/key-id-abc" {
				writeJSON(w, http.StatusOK, listResponse([]client.ObjectStoreAccessKey{}))
				return
			}
			writeJSON(w, http.StatusOK, listResponse([]client.ObjectStoreAccessKey{expected}))
		default:
			http.NotFound(w, r)
		}
	}))
	defer srv.Close()

	c := newTestClient(t, srv)
	got, err := c.GetObjectStoreAccessKey(context.Background(), "test-account/test-user/key-id-abc")
	if err != nil {
		t.Fatalf("GetObjectStoreAccessKey: %v", err)
	}
	if got.AccessKeyID != expected.AccessKeyID {
		t.Errorf("expected AccessKeyID %q, got %q", expected.AccessKeyID, got.AccessKeyID)
	}
	if !got.Enabled {
		t.Errorf("expected Enabled true")
	}
}

